let screen = document.getElementById('table');

function startWrite(i = 0, j = 0)
{
    if(i < to_write.length) {
        moveScreen();
        printColumn(to_write[i], j);
        j++;
        if(j > alphabet[to_write[i]][0].length) {
            i++;
            j = 0;
        }
        setTimeout(startWrite, 80, i, j);
    }
    else
        setTimeout(startWrite, 80);
}

function printColumn(letter, j)
{
    for(let i = 0; i < alphabet[letter].length; i++)
    {
        if(alphabet[letter][i][j] == 1)
            screen.rows[i+1].cells[cols-1].style = on;
    }
}

function moveScreen()
{
    for(let i = 0; i < cols-1; i++)
    {
        for(let j = 1; j < rows-1; j++)
        {
            if(screen.rows[j].cells[i+1].style.getPropertyValue("background-color") == 'transparent')
                screen.rows[j].cells[i].style = off;
            else
                screen.rows[j].cells[i].style = on;
        }
    }
    for(let i = 0; i < rows; i++)
        screen.rows[i].cells[cols-1].style = off;
}

function resetGrid()
{
    for(let i = 0; i < rows; i++)
        for(let j = 0; j < cols; j++)
            screen.rows[i].cells[j].style = off;
}

function parseString()
{
    to_write = document.getElementById("phrase").value + interrupt;
    to_write = to_write.toLowerCase();
    letterN = to_write.length;
    document.getElementById("input").remove();
    resetGrid();
    startWrite();
}

function createTable()
{
    let container = document.getElementById('board');
    let table = document.createElement('table');
    table.style = 'background-color: #2f2f2f'
    table.id = 'table'

    for (let i = 0; i < rows; i++) {
        let tr = document.createElement('tr');
        table.appendChild(tr);
        for (let j = 0; j < cols; j++) {
            let td = document.createElement('td');
            td.style = off;
            tr.appendChild(td);
        }
    }
    container.appendChild(table);
}